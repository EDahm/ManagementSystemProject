package Project;

public class Student {

	String name;
	String gender;
	String birth;
	String phnum;
	String type;
	int lecture;
	int score;

	
	public Student(String name, String gender, String birth, String phnum, String type, int lecture, int score) {
		this.name = name;
		this.gender = gender;
		this.birth = birth;
		this.phnum = phnum;
		this.type = type;
		this.lecture = lecture;
		this.score = score;
	}

	
	
	

	
	
	

	
}
